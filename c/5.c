
    #include<stdio.h>
    
    int main(){

    

        // +（加法）
        // -（减法）
        // *（乘法）
        // /（除法）
        // %（取余）
        // ++（自增）
        int a = 3;
        int b = ++a; // 前置递增：a 变为 4，b = 4
        int c = a++; // 后置递增：c = 4，a 变为 5

        int x = 5;
        int y = 10;
        
        float z = x + y;

        printf("%0.1f",z);

       return 0;
}